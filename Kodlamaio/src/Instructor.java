
public class Instructor extends User {
	
	private String placeOfDuty;
	private String lessonHour;
	
	public String getPlaceOfDuty() {
		return placeOfDuty;
	}
	public void setPlaceOfDuty(String placeOfDuty) {
		this.placeOfDuty = placeOfDuty;
	}
	public String getLessonHour() {
		return lessonHour;
	}
	public void setLessonHour(String lessonHour) {
		this.lessonHour = lessonHour;
	}
}
